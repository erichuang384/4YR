# ============================================================
# Build a helper function to join left + right blocks
# Each row: L1 L2 L3 L4 L5   R1 R2 R3 R4 R5
# ============================================================

function merge_lr(data)
    left = data[:, 1:5]
    right = data[:, 6:10]
    return vcat(left, right)
end

